def which_sensor(band):
  if ('VH' in band) or ('VV' in band) or ('_s1' in band): sensor = 'S1'
  elif ('HV' in band) or ('HH' in band) or ('_ss' in band): sensor = 'SS'
  elif band in TOPO_bands : sensor = 'TOPO'
  elif band in climate_bands : sensor = 'Climate'
  elif band in S2_bands: sensor = 'S2'
  elif band in label_bands: sensor = 'label'
  elif band in geo_bands: sensor = 'geo'
  elif 'rmse' in band: sensor = 'RMSE'
  else: sensor = band

  return sensor

